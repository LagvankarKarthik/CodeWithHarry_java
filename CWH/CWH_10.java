package CWH;

public class CWH_10 {
  public static void main(String[] args) {
    // Resulting Datatype
    byte x = 34;
    int y = 12;
    short z = 56;
    int a = x + y + z;
    float b = 3.142f + x / y;
    System.out.println(b);

    // Increment and decrement
    int i = 23;
    System.out.println(i++);
    System.out.println(i);
    System.out.println(++i);
    System.out.println(i);
  }
}